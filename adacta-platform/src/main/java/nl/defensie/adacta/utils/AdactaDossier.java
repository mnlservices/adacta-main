package nl.defensie.adacta.utils;

public class AdactaDossier {

	private String emplbsn;
	private String emplid;
	private String emplmrn;
	private String empltype;
	private String emplname;
	private String empldep;
	private String dpcodes;
	private String rowsecclass;
	
	public String getEmplbsn() {
		return emplbsn;
	}
	public void setEmplbsn(String emplbsn) {
		this.emplbsn = emplbsn;
	}
	public String getEmplid() {
		return emplid;
	}
	public void setEmplid(String emplid) {
		this.emplid = emplid;
	}
	public String getEmplmrn() {
		return emplmrn;
	}
	public void setEmplmrn(String emplmrn) {
		this.emplmrn = emplmrn;
	}
	public String getEmpltype() {
		return empltype;
	}
	public void setEmpltype(String empltype) {
		this.empltype = empltype;
	}
	public String getEmplname() {
		return emplname;
	}
	public void setEmplname(String emplname) {
		this.emplname = emplname;
	}
	public String getEmpldep() {
		return empldep;
	}
	public void setEmpldep(String empldep) {
		this.empldep = empldep;
	}
	public String getDpcodes() {
		return dpcodes;
	}
	public void setDpcodes(String dpcodes) {
		this.dpcodes = dpcodes;
	}
	public String getRowsecclass() {
		return rowsecclass;
	}
	public void setRowsecclass(String rowsecclass) {
		this.rowsecclass = rowsecclass;
	}
	
}
